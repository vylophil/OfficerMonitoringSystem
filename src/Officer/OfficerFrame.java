    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
                          
    try {
        // Check if ID field is filled
        if (txtid.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the Officer ID to update.");
            return;
        }

        // Get field values
        int id = Integer.parseInt(txtid.getText().trim());
        String fname = txtfname.getText().trim();
        String lname = txtlname.getText().trim();
        String mname = txtmname.getText().trim();
        String phno = txtphno.getText().trim();
        String gender = (String) txtgender.getSelectedItem();
        java.util.Date birthDate = txtbirth.getDate();

        if (fname.isEmpty() || lname.isEmpty() || mname.isEmpty() || phno.isEmpty() || birthDate == null) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields before updating!");
            return;
        }

        String formattedDate = new java.text.SimpleDateFormat("yyyy-MM-dd").format(birthDate);

        // Create Officer object
        Officer o = new Officer();
        o.setOfficerID(id);
        o.setFirstName(fname);
        o.setLastName(lname);
        o.setMiddleInitial(mname);
        o.setPhoneNumber(phno);
        o.setGender(gender);
        o.setBirthDate(formattedDate);

        // Update using DAO
        OfficerDAO dao = new OfficerDAO();
        dao.updateOfficer(o);

        JOptionPane.showMessageDialog(this, "Officer updated successfully!");
         loadOfficerData();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error updating officer: " + e.getMessage());
    }

 
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
                                  
    try {
        if (txtid.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the Officer ID to delete.");
            return;
        }

        int id = Integer.parseInt(txtid.getText().trim());

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to delete Officer ID " + id + "?",
                "Confirm Deletion",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            OfficerDAO dao = new OfficerDAO();
            dao.deleteOfficer(id);
            JOptionPane.showMessageDialog(this, "Officer deleted successfully!");
            
            loadOfficerData();
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error deleting officer: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton4ActionPerformed

