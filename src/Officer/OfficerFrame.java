    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                                                                             
    String fname = txtfname.getText().trim();
    String lname = txtlname.getText().trim();
    String mname = txtmname.getText().trim();
    String phno = txtphno.getText().trim();
    String gender = (String) txtgender.getSelectedItem();
    
    java.util.Date birthDate = txtbirth.getDate();
    String formattedDate = "";
    if (birthDate != null) {
        formattedDate = new java.text.SimpleDateFormat("yyyy-MM-dd").format(birthDate);
    }

    if (fname.isEmpty() || lname.isEmpty() || mname.isEmpty() || phno.isEmpty() || birthDate == null) {
        JOptionPane.showMessageDialog(null, "Please fill in all fields!");
        return;
    }

    Officer o = new Officer();
    o.setFirstName(fname);
    o.setLastName(lname);
    o.setMiddleInitial(mname);
    o.setPhoneNumber(phno);
    o.setGender(gender);
    o.setBirthDate(formattedDate);

    OfficerDAO dao = new OfficerDAO();
    dao.addOfficer(o);

    JOptionPane.showMessageDialog(null, "Officer saved successfully!");
    //Loads the table
     loadOfficerData();

    // clear fields
    txtfname.setText("");
    txtlname.setText("");
    txtmname.setText("");
    txtphno.setText("");
    txtbirth.setDate(null);
    txtgender.setSelectedIndex(0);

    }//GEN-LAST:event_jButton2ActionPerformed

